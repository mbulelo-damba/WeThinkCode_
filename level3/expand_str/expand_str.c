#include <unistd.h>

int		main(int ac, char **av)
{
	int		i;
	int		j;

	i = 1;
	j = 0;
	if(ac == 2)
	{
		while(*av[1] != '\0')
		{
			if(*av[1] == ' ' || *av[1] == '\t')
				i = 0;
			else
			{
				if(j == 1 && i == 0)
					write(1, "   ", 3);
				j = 1;
				i = 1;
				write(1, av[1], 1);
			}
			av[1]++;
		}
	}
	write(1, "\n", 1);
	return(0);
}
